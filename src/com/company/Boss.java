package com.company;

public class Boss {
    private int bosshealt;
    private int bossdamage;
    private String bossdefense;

    public int getBosshealt() {
        return bosshealt;
    }

    public void setBosshealt(int bosshealt) {
        this.bosshealt = bosshealt;
    }

    public int getBossdamage() {
        return bossdamage;
    }

    public void setBossdamage(int bossdamage) {
        this.bossdamage = bossdamage;
    }

    public String getBossdefense() {
        return bossdefense;
    }

    public void setBossdefense(String bossdefense) {
        this.bossdefense = bossdefense;
    }
}

